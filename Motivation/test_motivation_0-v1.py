def get_reward(outputs):
    reward = 0
    rewards = []

    for output in outputs:
        if 'yes' in output.lower():
            rewards.append(1)
        else:
            rewards.append(-1)
    if -1 in rewards:
        reward = -1
    else:
        reward = 1
    print(f"Reward: {reward}")
    return reward

