def call_api(payload: dict):
    """
    一个被修改的 "API" 调用函数。
    它会拦截本地模型的生成任务，并直接处理。
    对于其他任务或模型，它会退回到原始的网络请求逻辑（如果需要的话）。
    """
    global SFT_MODEL, SFT_TOKENIZER, DEVICE

    # a. 检查这是否是我们想要拦截的本地SFT模型任务
    # 在这个脚本中，只有 get_system_response 会调用 call_api
    # 并且 payload['task_type'] 会是 "generate"
    if payload.get("task_type") == "generate" and SFT_MODEL is not None:
        print("拦截到本地SFT模型生成任务，正在直接处理...")

        # b. 从payload中提取messages，并构建Prompt
        # 这里的逻辑需要适配 get_system_messages_v4 的输出
        system_messages = payload["messages"]
        # get_system_messages_v4 返回的列表中，最后一个user message包含了完整的Prompt内容
        prompt = system_messages[-1]['content']

        # c. 使用本地模型进行生成
        inputs = SFT_TOKENIZER(prompt, return_tensors="pt").to(DEVICE)
        with torch.no_grad():
            # with torch.cuda.amp.autocast(dtype=torch.bfloat16):
            #     outputs = SFT_MODEL.generate(
            #         **inputs,
            #         max_new_tokens=128,
            #         num_beams=3,
            #         do_sample=False,
            #         pad_token_id=SFT_TOKENIZER.eos_token_id
            #     )
            with torch.cuda.amp.autocast(dtype=torch.bfloat16):
                outputs = SFT_MODEL.generate(
                    **inputs,
                    max_new_tokens=128,
                    # [核心修改] 从束搜索切换到随机采样
                    do_sample=True,      # 启用采样
                    temperature=0.7,     # 控制随机性（0.7-0.9是不错的起点），值越高越随机
                    top_p=0.9,           # 核采样，只在概率最高的90%的词汇中进行选择，防止生成胡话
                    pad_token_id=SFT_TOKENIZER.eos_token_id
                    # num_beams=3,       # 启用采样时，通常不与 beam search 同时使用
                    )

        response_ids = outputs[0][len(inputs.input_ids[0]):]
        response_text = SFT_TOKENIZER.decode(response_ids, skip_special_tokens=True).strip()

        # d. [关键] 返回一个与原始API格式完全相同的伪响应
        # 我们的 only-response 模型不生成策略，所以我们只填充 response 字段
        # 为了让 get_system_response 函数能正常工作，我们伪造一个空的 "strategy" 和 "reason"
        # get_system_response 期待一个能被 json.loads 解析的字符串
        pseudo_json_string = json.dumps({
            "strategy": "inferred_from_response", # 伪造的策略
            "response": response_text,
            "reason": "Generated by local SFT model." # 伪造的理由
        })

        return {
            "generated_texts": [pseudo_json_string]
        }

    # e. 如果不是我们要拦截的任务，就执行原始的网络请求逻辑
    # 在这个脚本中，这个分支可能不会被执行，但保留下来以保证兼容性
